#List Indexing positive and negative indexing
# is same as string indexing
my_list = [1, 'Hello', 3, False, 598.9]
#here 2-index is 3
print(my_list[2]) #to access the third element of the list
print(my_list[-1]) #to access the last element of the list
print(my_list[-2]) #to access the second last element of the list